#include <stdio.h>

int main()
{

    char c = 't';
    int d = -123;
    int i = -321;
    long long int lld = -987654321;
    unsigned int u = 12345;
    unsigned long long int llu = 9876554321;
    float f = 123.456;
    double lf = 12345.6789;


    printf("%c\n", c);
    printf("%d\n", d);
    printf("%i\n", i);
    printf("%lld\n", lld);
    printf("%u\n", u);
    printf("%llu\n", llu);
    printf("%f\n", f);
    printf("%lf\n", lf);

    return 0;
}
